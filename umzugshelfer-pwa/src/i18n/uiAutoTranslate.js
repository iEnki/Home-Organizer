function redirectedLocale(locale, i18n) {
  if (normalizeLocale(i18n.language) !== "en-GB") return locale;
  if (typeof locale === "string" && /^de(?:-|$)/i.test(locale)) return "en-GB";
  if (Array.isArray(locale)) {
    return locale.map((entry) => (typeof entry === "string" && /^de(?:-|$)/i.test(entry) ? "en-GB" : entry));
  }
  return locale;
}

