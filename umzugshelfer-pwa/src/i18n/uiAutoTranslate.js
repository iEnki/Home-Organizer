function withWhitespace(source, translated) {
  const start = source.match(/^\s*/)?.[0] || "";
  const end = source.match(/\s*$/)?.[0] || "";
  return `${start}${translated}${end}`;
}

export function translateUiText(value, locale) {
  if (normalizeLocale(locale) !== "en-GB" || typeof value !== "string") return value;
  const trimmed = value.trim();
  if (!trimmed) return value;

  const exact = EXACT_EN_GB[trimmed];
  if (exact) return withWhitespace(value, exact);

  for (const [pattern, replacement] of PATTERN_EN_GB) {
    if (pattern.test(trimmed)) {
      return withWhitespace(value, trimmed.replace(pattern, replacement));
    }
  }

  return value;
}

function translateTextNode(node, locale) {
  if (!node?.parentElement || SKIP_TAGS.has(node.parentElement.tagName)) return;
  const translated = translateUiText(node.nodeValue, locale);
  if (translated !== node.nodeValue) node.nodeValue = translated;
}

function translateElementAttributes(element, locale) {
  if (!element?.getAttribute || SKIP_TAGS.has(element.tagName)) return;
  for (const attr of ATTRIBUTES) {
    const value = element.getAttribute(attr);
    if (!value) continue;
    const translated = translateUiText(value, locale);
    if (translated !== value) element.setAttribute(attr, translated);
  }
}

function walk(root, locale) {
  if (!root) return;
  if (root.nodeType === Node.TEXT_NODE) {
    translateTextNode(root, locale);
    return;
  }
  if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_NODE) return;

  if (root.nodeType === Node.ELEMENT_NODE) translateElementAttributes(root, locale);

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT);
  let current = walker.nextNode();
  while (current) {
    if (current.nodeType === Node.TEXT_NODE) translateTextNode(current, locale);
    if (current.nodeType === Node.ELEMENT_NODE) translateElementAttributes(current, locale);
    current = walker.nextNode();
  }
}

